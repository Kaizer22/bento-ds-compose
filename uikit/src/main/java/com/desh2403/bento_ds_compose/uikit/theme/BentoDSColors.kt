    val informative: Color = informative_5,
    // TODO inverse
    val positive: Color = positive_5,
    // TODO inverse
    val warning: Color = warning_5,
    // TODO inverse
    val negative: Color = negative_5,
    val scrim: Color,
    // endregion
)

